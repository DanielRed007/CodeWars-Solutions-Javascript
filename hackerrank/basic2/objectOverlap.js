function boredom(staff = {}){
    const scores = {
        "accounts": 1,
        "finance": 2,
        "canteen": 10,
        "regulation": 3,
        "trading": 6,
        "change": 6,
        "IS": 8,
        "retail": 5,
        "cleaning": 4,
        "pissing about": 25
    };
    
    let result = 0;

    for(let status in staff){
        result += scores[staff[status]]
    }

    if(result <= 80) return "kill me now"
    if(result < 100 && result > 80) return "i can handle this"
    if(result >= 100) return "party time!!"
}   

